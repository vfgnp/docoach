def clean_text(content):
    text = re.sub(r'.*?-{30,}.*?-{30,}', '', content, flags=re.DOTALL)
    text = re.sub(r'\n底本：.*', '', text, flags=re.DOTALL)
    text = re.sub(r'《[^》]*》', '', text)
    text = re.sub(r'｜', '', text)
    text = re.sub(r'［＃[^\]]*］', '', text)
    text = re.sub(r'\n{3,}', '\n\n', text)
    return text.strip()

def get_title(content):
    lines = content.strip().split('\n')
    t = lines[0].strip()
    return t if '小川未明' not in t and len(t) < 40 else ''

def get_excerpt(clean, max_chars=250):
    paras = [p.strip() for p in clean.split('\n') if p.strip() and len(p.strip()) > 30
             and '底本' not in p and '入力' not in p and '青空文庫' not in p]
    if not paras:
        return clean[:max_chars]
    # Pick paragraphs starting from 1/4 through the text
    start = max(0, len(paras)//4)
    result = ''
    for p in paras[start:]:
        if len(result) + len(p) <= max_chars:
            result += p + '\n'
        if len(result) >= 100:
            break
    if not result:
        result = paras[0][:max_chars]
    return result.strip()[:max_chars]

