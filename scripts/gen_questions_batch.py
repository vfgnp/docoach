def process_file(fname, file_index, content):
    tag1 = TAG_SCHEDULE[(file_index * 2) % 22]
    tag2 = TAG_SCHEDULE[(file_index * 2 + 1) % 22]
    clean = clean_text(content)
    title = get_title(content)
    excerpt = get_excerpt(clean)

    if fname in MANUAL:
        qs = MANUAL[fname](excerpt, title)
        return qs

    q1 = make_generic(excerpt, title, tag1, file_index * 2)
    q2 = make_generic(excerpt, title, tag2, file_index * 2 + 1)
    return [q1, q2]

def main():
    with open(BUNDLE_PATH, 'r', encoding='utf-8') as f:
        bundle = json.load(f)
    existing = bundle['questions']
    print(f'既存問題数: {len(existing)}')

    files = get_files()
    print(f'対象ファイル数: {len(files)}')

    new_qs = []
    for i, fname in enumerate(files):
        fpath = os.path.join(OUTPUT_DIR, fname)
        content = open(fpath, encoding='utf-8').read()
        qs = process_file(fname, i, content)
        new_qs.extend(qs)
        if (i + 1) % 10 == 0:
            all_q = existing + new_qs
            with open(BUNDLE_PATH, 'w', encoding='utf-8') as f:
                json.dump({'questions': all_q}, f, ensure_ascii=False, indent=2)
            print(f'  {i+1}/{len(files)} 処理済み, 合計{len(all_q)}問 -> 保存完了')

    all_q = existing + new_qs
    with open(BUNDLE_PATH, 'w', encoding='utf-8') as f:
        json.dump({'questions': all_q}, f, ensure_ascii=False, indent=2)
    print(f'\n完了! 合計問題数: {len(all_q)}問 (新規: {len(new_qs)}問)')

if __name__ == '__main__':
    main()
