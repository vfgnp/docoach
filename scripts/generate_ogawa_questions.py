def extract_passage(text, max_chars=280, min_chars=150):
    """本文から適切な抜粋を取る"""
    # 段落に分割
    paragraphs = [p.strip() for p in text.split('\n') if p.strip()]

    # 本文らしい段落を探す（ヘッダー等を除く）
    content_paragraphs = []
    for p in paragraphs:
        if len(p) > 30 and not p.startswith('底本') and not p.startswith('入力') and not p.startswith('校正') and '青空文庫' not in p:
            content_paragraphs.append(p)

    if not content_paragraphs:
        return text[:max_chars]

    # 中盤から適切な段落を選ぶ
    mid = len(content_paragraphs) // 3
    passage = ''
    for p in content_paragraphs[mid:mid+3]:
        if len(passage) + len(p) <= max_chars:
            passage += p + '\n'
        if len(passage) >= min_chars:
            break

    if len(passage) < min_chars and content_paragraphs:
        passage = content_paragraphs[0][:max_chars]

    return passage.strip()[:max_chars]

