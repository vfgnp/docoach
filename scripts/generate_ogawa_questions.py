def clean_text(text):
    """ルビ記号などを除去して読みやすいテキストにする"""
    # ヘッダー部分を除去
    text = re.sub(r'.*?-{30,}.*?-{30,}', '', text, flags=re.DOTALL)
    # 底本情報を除去
    text = re.sub(r'\n底本：.*', '', text, flags=re.DOTALL)
    # ルビを除去: 《よみ》 → ''
    text = re.sub(r'《[^》]*》', '', text)
    # ルビ開始記号除去
    text = re.sub(r'｜', '', text)
    # 傍点等の注記除去
    text = re.sub(r'［＃[^\]]*］', '', text)
    # 空行を整理
    text = re.sub(r'\n{3,}', '\n\n', text)
    text = text.strip()
    return text

