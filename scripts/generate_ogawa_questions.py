def main():
    # 既存のbundleを読み込む
    with open(BUNDLE_PATH, 'r', encoding='utf-8') as f:
        bundle = json.load(f)

    existing_questions = bundle['questions']
    print(f'既存問題数: {len(existing_questions)}')

    # 対象ファイルを取得
    files = get_files()
    print(f'対象ファイル数: {len(files)}')

    new_questions = []

    for i, fname in enumerate(files):
        fpath = os.path.join(OUTPUT_DIR, fname)
        with open(fpath, 'r', encoding='utf-8') as f:
            content = f.read()

        # タイトルを取得
        title = get_title_from_content(content)

        # 問題を生成
        qs = generate_questions_for_file(fname, i, content, title)
        new_questions.extend(qs)

        if (i + 1) % 10 == 0:
            print(f'処理済み: {i + 1}/{len(files)} ファイル, 生成問題数: {len(new_questions)}')
            # バックアップ書き出し
            all_questions = existing_questions + new_questions
            with open(BUNDLE_PATH, 'w', encoding='utf-8') as f:
                json.dump({'questions': all_questions}, f, ensure_ascii=False, indent=2)
            print(f'  -> 中間保存完了 (合計{len(all_questions)}問)')

    # 最終書き出し
    all_questions = existing_questions + new_questions
    with open(BUNDLE_PATH, 'w', encoding='utf-8') as f:
        json.dump({'questions': all_questions}, f, ensure_ascii=False, indent=2)

    print(f'\n完了！合計問題数: {len(all_questions)}問 (新規追加: {len(new_questions)}問)')

if __name__ == '__main__':
    main()
